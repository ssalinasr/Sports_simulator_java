    private void BtnGenerateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnGenerateActionPerformed
        int from_year = Integer.parseInt((String) this.BoxFrom.getSelectedItem());
        int to_year = Integer.parseInt((String) this.BoxTo.getSelectedItem());
        int period = Integer.parseInt((String) this.BoxPeriods.getSelectedItem());

        if (this.BtnCheckAll.isSelected()) {
            this.sim_frame.setBoxAll(true);
            this.sim_frame.updateTeams();
        } else {
            this.sim_frame.setBoxAll(false);
            this.sim_frame.updateTeams();
        }

        for (int p = from_year; p <= to_year; p += period) {
            this.table_models.clear();
            String game = (String) this.BoxGame.getSelectedItem();
            System.out.println(game);
            this.sim_frame.setGame(game);
            this.current_game_box = this.sim_frame.getGameBox();
            this.current_category_box = this.sim_frame.getCategoryBox();
            this.getCategories();
            System.out.println(this.current_categories);

            for (String cat : this.current_categories) {
                this.sim_frame.setCategory(cat);
                this.current_sport_box = this.sim_frame.getSportBox();
                this.getSports();
                for (String sport : this.current_sports) {
                    this.sim_frame.setSport(sport);
                    this.sim_frame.simulateSport();
                    ArrayList<Object> objs = new ArrayList<>();
                    objs.add(this.sim_frame.getSimTable());
                    objs.add(sport);
                    this.table_models.add(objs);
                    System.out.println(this.current_sport_box.getSelectedItem() + ": ");
                    System.out.println(this.sim_frame.getSimTable());

                }
            }

            String fileName = this.TxtFileName.getText();
            if (fileName.isEmpty()) {
                fileName = "dumptest";
            }
            String filePath = "export/" + fileName + p + ".xls";

            try {
                this.exportToExcel(this.table_models, filePath);
                this.LblStatus.setText("El archivo " + fileName + p + " ha sido generado...");
            } catch (IOException ex) {
                this.LblStatus.setText("El archivo no ha sido generado...");
            }
        }


    }//GEN-LAST:event_BtnGenerateActionPerformed

