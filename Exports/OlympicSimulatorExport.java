    public void exportToExcel(ArrayList<ArrayList<Object>> tables, String filePath) throws FileNotFoundException, IOException {
        System.out.println(filePath);
        Workbook workbook = new HSSFWorkbook();
        Row row;
        Cell cell;

        ArrayList<DefaultTableModel> table = new ArrayList<>();
        ArrayList<String> sports_names = new ArrayList<>();

        for (ArrayList<Object> obj : tables) {
            table.add((DefaultTableModel) obj.get(0));
            sports_names.add((String) obj.get(1));
        }

        int index = 0;

        for (DefaultTableModel mdl : table) {
            DefaultTableModel model = mdl;
            Sheet sheet = workbook.createSheet(sports_names.get(index));
            // write the column headers
            row = sheet.createRow(0);
            for (int c = 0; c < model.getColumnCount(); c++) {
                cell = row.createCell(c);
                cell.setCellValue(model.getColumnName(c));
            }

            // write the data rows
            for (int r = 0; r < model.getRowCount(); r++) {
                row = sheet.createRow(r + 1);
                for (int c = 0; c < model.getColumnCount(); c++) {
                    cell = row.createCell(c);
                    Object value = model.getValueAt(r, c);
                    if (value instanceof String) {
                        cell.setCellValue((String) value);
                    } else if (value instanceof Double) {
                        cell.setCellValue((Double) value);
                    }else if (value instanceof Integer) {
                        cell.setCellValue((Integer) value);
                    }
                }
            }
            index++;
        }

        FileOutputStream out = new FileOutputStream(filePath);
        workbook.write(out);
        out.close();
        workbook.close();

    }

